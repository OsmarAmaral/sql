
CREATE TABLE tbSala (
	codigo_sala INT PRIMARY KEY,
	descricao_sala VARCHAR (30),
	capacidade_sala INT
 );
 
 CREATE TABLE tbDiretor(
	codigo_diretor INT PRIMARY KEY,
	nome_diretor VARCHAR (30)
 );
 
 CREATE TABLE tbFilme(
	codigo_filme INT PRIMARY KEY,
	nome_filme VARCHAR (50),
	ano_filme INT,
	categoria_filme VARCHAR(20),
	codigo_diretor INT,
	FOREIGN KEY (codigo_diretor) REFERENCES tbDiretor (codigo_diretor)
 );
 
CREATE TABLE tbPremio(
	codigo_premio INT PRIMARY KEY,
	nome_premio VARCHAR(40)
 );

CREATE TABLE tbPremiacao(
	codigo_premiacao INT PRIMARY KEY,
	codigo_premio INT,
	codigo_filme INT,
	ano_premiacao INT,
	FOREIGN KEY (codigo_premio) REFERENCES tbPremio (codigo_premio),
	FOREIGN KEY (codigo_filme) REFERENCES tbFilme (codigo_filme)
 );
 
CREATE TABLE tbSessao (
	codigo_sessao INT PRIMARY KEY,
	codigo_filme INT,
	codigo_sala INT,
	data_sessao DATE,
	horario_sessao TIME,
	FOREIGN KEY (codigo_filme) REFERENCES tbFilme (codigo_filme),
	FOREIGN KEY (codigo_sala) REFERENCES tbSala (codigo_sala)
 );

CREATE TABLE tbEspectador (
	codigo_espec INT PRIMARY KEY,
	nome_espec VARCHAR(50),
	idade_espec INT,
	sexo_espec VARCHAR(9)
);

CREATE TABLE tbIngresso (
	codigo_ingresso INT PRIMARY KEY,
	codigo_sessao INT,
	codigo_espec INT, 
	valor_ingresso DECIMAL,
	FOREIGN key (codigo_sessao) REFERENCES tbSessao (codigo_sessao),
	FOREIGN key (codigo_espec) REFERENCES tbEspectador (codigo_espec)
 );
 
